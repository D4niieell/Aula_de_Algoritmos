# Solicitar ao usuario um numero de 1 até 15
# Gerar um numero aleatorio usando a biblioteca random.randiant
# Verificar se o usuario digitou o mesmo valor que o resultado da função randiant

import random

numero_usuario = int(input("Digite um numero da sorte de 1 até 5: "))
numero_sorte = random.randint
if numero_usuario == numero_sorte:
    print("Parabens Fulano, você ganhou um aperto de mão e um dois reais")
else: 
    print("Errou, tente novamente") 
